def create_daily_commits(date):
    num_commits = random.randint(0, 1)
    for i in range(num_commits):
        commit_date = date + timedelta(minutes=random.randint(0, 1440))
        commit_message = random.choice(commit_messages)
        print(f"Adding commit at: {commit_date.strftime('%Y-%m-%d %H:%M:%S')} - Message: {commit_message}")
        # Thiáº¿t láº­p thá»i gian commit trong mÃ´i trÆ°á»ng
        os.environ['GIT_AUTHOR_DATE'] = commit_date.isoformat()
        os.environ['GIT_COMMITTER_DATE'] = commit_date.isoformat()

        # Táº¡o commit giáº£
        subprocess.run(["git", "commit", "--allow-empty", "-m", commit_message])


